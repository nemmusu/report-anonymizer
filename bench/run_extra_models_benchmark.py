"""Bench every GGUF in ``$MODELS_DIR/_bench_downloads.json``.

Reads the manifest the sequential downloader wrote, constructs an
in-memory ``ServerProfile`` for each successful download, runs the
existing precision harness ``run_one`` per PDF in ``BENCH_CORPUS_ROOT``,
scores against ``$BENCH_ROOT/groundtruth/groundtruth.yml``, and dumps
both ``report.json`` (machine-readable, consumed when patching the
catalog / BENCHMARKS.md) and ``report.md`` (eyeball summary).

Usage
-----
    BENCH_CORPUS_ROOT=/path/to/pdfs \
        .venv/bin/python bench/run_extra_models_benchmark.py \
        --out-root /tmp/anonbench/extra_models

Each model's per-pdf rc / caught / score lives under
``out-root/<safe_name>/`` so individual missing-leak lists are
preserved for the catalog descriptions.
"""
from __future__ import annotations

import argparse
import json
import os
import re
import sys
import time
from pathlib import Path
from typing import Optional

import yaml

REPO = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO))

from anonymize.server_manager import ServerManager
from anonymize.server_profile import ServerProfile, SamplingConfig

from bench.run_precision_benchmark import (
    GT_PATH,
    _peak_memory_sample,
    _resolve_corpus_root,
    load_ground_truth,
    run_one,
    score,
)


MODELS_DIR = Path.home() / ".local/share/document-anonymizer/models"
MANIFEST = MODELS_DIR / "_bench_downloads.json"


def _safe_name(repo: str, fname: str) -> str:
    """Stable, filename-friendly preset name for a (repo, file) pair."""
    base = fname.lower().removesuffix(".gguf")
    base = re.sub(r"[^a-z0-9]+", "-", base).strip("-")
    return base[:64]


def _build_profile(repo: str, fname: str) -> ServerProfile:
    """In-memory profile pointing at the downloaded GGUF.

    Reuses the canonical ``default`` shape (host / port / sampling
    parameters) and only overrides the model path + the fields that
    are likely to vary per model. ``ctx_size=12288 / parallel=1``
    keeps the slot well above the detector's ~7 400-token requirement
    even for chunked-context-light models.
    """
    safe_repo = repo.replace("/", "__")
    model_path = MODELS_DIR / safe_repo / fname
    name = _safe_name(repo, fname)
    return ServerProfile(
        name=name,
        description=f"bench: {repo} / {fname}",
        is_builtin=False,
        binary="/home/nemmusu/llama.cpp/build/bin/llama-server",
        host="127.0.0.1",
        port=8080,
        model=str(model_path),
        model_repo=repo,
        model_filename=fname,
        ctx_size=12288,
        parallel=1,
        n_gpu_layers=99,
        threads=18,
        batch_size=8192,
        ubatch_size=512,
        flash_attn=True,
        mmap=True,
        no_warmup=True,
        cache_prompt=True,
        cache_type_k="f16",
        cache_type_v="f16",
        chat_template=None,
        jinja=True,
        no_webui=True,
        sampling=SamplingConfig(
            temperature=0.3, top_k=20, top_p=0.8,
            min_p=0.0, repeat_penalty=1.0, presence_penalty=0.0,
        ),
    )


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--out-root", type=Path,
        default=Path("/tmp/anonbench/extra_models"),
    )
    ap.add_argument(
        "--manifest", type=Path, default=MANIFEST,
        help=(
            "Path to the bench manifest JSON. Defaults to "
            f"{MANIFEST}, the file the sequential downloader writes; "
            "point this at a hand-crafted manifest (e.g. "
            "_bench_user_models.json) to bench a curated subset "
            "without touching the autogenerated list."
        ),
    )
    ap.add_argument("--per-pdf-timeout", type=int, default=600)
    ap.add_argument("--start-timeout", type=int, default=180)
    args = ap.parse_args()

    manifest_path: Path = args.manifest
    if not manifest_path.exists():
        print(f"[fatal] no manifest at {manifest_path}", file=sys.stderr)
        return 2
    manifest = json.loads(manifest_path.read_text())
    bench_specs = [
        m for m in manifest if m.get("status") in ("ok", "cached")
    ]
    if not bench_specs:
        print("[fatal] no successfully-downloaded models to bench", file=sys.stderr)
        return 2

    args.out_root.mkdir(parents=True, exist_ok=True)
    corpus = _resolve_corpus_root()
    gt = load_ground_truth(GT_PATH)
    pdfs = [corpus / name for name in gt.keys() if (corpus / name).exists()]
    if not pdfs:
        print(f"[fatal] no GT PDFs in {corpus}", file=sys.stderr)
        return 2
    print(f"corpus: {len(pdfs)} PDF(s); models: {len(bench_specs)}")

    results: dict[str, dict] = {}
    for spec in bench_specs:
        repo = spec["repo"]; fname = spec["file"]
        prof = _build_profile(repo, fname)
        print(f"\n==== {prof.name}  ({repo}/{fname}) ====")
        prof_out = args.out_root / prof.name
        prof_out.mkdir(parents=True, exist_ok=True)

        mgr = ServerManager(prof)
        try:
            mgr.stop(timeout=5.0)
        except Exception:
            pass
        t0 = time.time()
        try:
            ok = mgr.start(wait_seconds=args.start_timeout)
        except Exception as e:
            ok = False
            err = str(e)[:200]
        startup_s = round(time.time() - t0, 1)
        if not ok:
            # ``_log`` on ServerManager is a ``deque`` which does NOT
            # support slicing; coerce to list before tailing.
            log_lines = list(getattr(mgr, "_log", []) or [])[-3:]
            err_log = "\n".join(log_lines)[-400:]
            print(f"  [FAIL] not ready in {args.start_timeout}s  log_tail={err_log!r}")
            results[prof.name] = {
                "repo": repo, "file": fname, "started": False,
                "startup_s": startup_s, "per_pdf": {},
                "size_bytes": spec.get("bytes", 0),
            }
            continue
        print(f"  ready in {startup_s}s")

        server_pid = getattr(mgr, "pid", None) or getattr(mgr, "_pid", None)
        idle_vram, _ = _peak_memory_sample(server_pid)

        per_pdf: dict[str, dict] = {}
        peak_vram = idle_vram
        total_elapsed = 0.0
        for pdf in pdfs:
            t = time.time()
            outdir = prof_out / pdf.stem
            rc, caught, _ = run_one(
                pdf, outdir,
                llm_url=f"http://{prof.host}:{prof.port}/v1",
                llm_model=prof.name,
                timeout_s=args.per_pdf_timeout,
            )
            elapsed = round(time.time() - t, 1)
            total_elapsed += elapsed
            v, _ = _peak_memory_sample(server_pid)
            peak_vram = max(peak_vram, v)
            sc = score(gt[pdf.name], caught)
            per_pdf[pdf.name] = {
                "rc": rc, "elapsed": elapsed,
                "caught": len(caught), "gt": len(gt[pdf.name]),
                **sc,
            }
            print(
                f"  · {pdf.name}  rc={rc:3d}  {elapsed:5.1f}s  "
                f"caught={len(caught):3d}  gt={len(gt[pdf.name]):2d}  "
                f"P={sc['precision']:.2f}  R={sc['recall']:.2f}  F1={sc['f1']:.2f}"
            )
        try:
            mgr.stop(timeout=5.0)
        except Exception:
            pass

        # Aggregate
        agg_tp = sum(d["tp"] for d in per_pdf.values())
        agg_fp = sum(d["fp"] for d in per_pdf.values())
        agg_fn = sum(d["fn"] for d in per_pdf.values())
        prec = agg_tp / (agg_tp + agg_fp) if (agg_tp + agg_fp) else 0.0
        rec = agg_tp / (agg_tp + agg_fn) if (agg_tp + agg_fn) else 0.0
        f1 = (2 * prec * rec / (prec + rec)) if (prec + rec) else 0.0
        quality = int(round(f1 * 100))
        # Live aggregate line so external monitors see the headline
        # number as soon as each model finishes its 5 PDFs (rather
        # than waiting for the JSON dump at the very end of the run).
        print(
            f"  >> AGG {prof.name}  quality={quality:3d}/100  "
            f"P={prec:.3f}  R={rec:.3f}  F1={f1:.3f}  "
            f"VRAM={peak_vram}MB  total={total_elapsed:.0f}s"
        )
        results[prof.name] = {
            "repo": repo, "file": fname, "started": True,
            "startup_s": startup_s, "size_bytes": spec.get("bytes", 0),
            "per_pdf": per_pdf,
            "agg": {
                "tp": agg_tp, "fp": agg_fp, "fn": agg_fn,
                "precision": round(prec, 3),
                "recall": round(rec, 3),
                "f1": round(f1, 3),
                "quality": int(round(f1 * 100)),
            },
            "peak_vram_mb": peak_vram,
            "total_elapsed_s": round(total_elapsed, 1),
        }

    out_json = args.out_root / "report.json"
    out_json.write_text(json.dumps(results, indent=2))
    out_md = args.out_root / "report.md"
    lines = [
        "# Extra-models bench",
        "",
        f"Corpus: {len(pdfs)} PDFs.  Ran {len(bench_specs)} candidate(s).",
        "",
        "| profile | started | quality | precision | recall | F1 | disk | peak VRAM | total |",
        "|---|---|---|---|---|---|---|---|---|",
    ]
    for n, r in sorted(results.items(), key=lambda kv: -(kv[1].get("agg", {}).get("quality") or 0)):
        if not r["started"]:
            lines.append(
                f"| `{n}` | ❌ |, |, |, |, | "
                f"{r['size_bytes']/2**30:.1f} GB |, |, |"
            )
            continue
        a = r["agg"]
        lines.append(
            f"| `{n}` | ✅ | {a['quality']} / 100 | {a['precision']:.2%} "
            f"| {a['recall']:.2%} | {a['f1']:.2%} | "
            f"{r['size_bytes']/2**30:.1f} GB | {r['peak_vram_mb']} MB | "
            f"{r['total_elapsed_s']:.0f} s |"
        )
    out_md.write_text("\n".join(lines) + "\n")
    print(f"\nreport -> {out_md}")
    print(f"json   -> {out_json}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
